const dicts: Record<Lang, Record<string, string>> = { en, pt };

let current: Lang = "en";

export function detectLang(): Lang {
  try {
    const l = (window.localStorage.getItem("language") || "").toLowerCase();
    return l.startsWith("pt") ? "pt" : "en";
  } catch {
    return "en";
  }
}

export function setLanguage(pref: LangPref): void {
  current = pref === "auto" ? detectLang() : pref;
}

export function getLanguage(): Lang {
  return current;
}

export function t(key: string, vars?: Record<string, string | number>): string {
  const d = dicts[current] || en;
  let s = d[key] ?? en[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) s = s.split(`{${k}}`).join(String(vars[k]));
  }
  return s;
}
